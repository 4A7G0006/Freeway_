def get_data(weather_folder, place, years):
    for space in place:
        with open(weather_folder + '\\' + space + "氣象.txt", "r", encoding='utf-8-sig')as weatherfile:
            area = weatherfile.read().splitlines()
        for year_ in years:
            with open(weather_folder + "\\date\\" + str(year_) + ".txt", "r", encoding='utf-8-sig')as dayfile:
                days = dayfile.read().splitlines()
                for region in area:
                    data = region.split(",", maxsplit=1)
                    # header = header_change  # 換個user agent
                    savepath = weather_folder + '\\' + space + '\\' + str(year_) + '\\' + str(data[1]) + '\\'
                    if not os.path.exists(savepath):
                        os.makedirs(savepath)
                    else:
                        pass  # 存在過路徑就不在重新生成
                    for day in days:
                        yy_mm_dd = str(day[0:4]) + "-" + str(day[4:6]) + "-" + str(day[6:8])
                        if not os.path.exists(savepath + yy_mm_dd + ".csv"):
                            url = "https://e-service.cwb.gov.tw/HistoryDataQuery/DayDataController.do?command=viewMain&station=" + str(data[0]) + "&stname=" + urllib.parse.quote(
                                urllib.parse.quote(data[1])) + "&datepicker=" + yy_mm_dd
                            print(url)
                            # content = requests.get(url, verify=False, headers=header) #使用requests 太慢
                            driver.get(url)
                            container = BeautifulSoup(driver.page_source, "html.parser")
                            table = container.find("table", {"id": "MyTable"})
                            rows = table.find_all("tr")
                            csvFile = open(savepath + yy_mm_dd + ".csv", 'w', newline='', encoding='utf-8-sig')
                            writer = csv.writer(csvFile)
                            try:
                                for num, row in enumerate(rows):
                                    csvRow = []
                                    if num > 0:  # 第一行資料不要      #Press 	temperature	dew point	RH	WD/WS	Precp	SunShine	visibility	UVI	Cloud
                                        all = row.find_all('th') if num < 3 else row.find_all('td')
                                        for about in all:
                                            csvRow.append(about.get_text() if num < 3 else about.get_text().replace("\xa0", ""))
                                        # print(csvRow)
                                        writer.writerow(csvRow)
                                print("--" + savepath[1:] + yy_mm_dd + ".csv\t\tSucessful !")
                            except:
                                print("--" + savepath[1:] + yy_mm_dd + ".csv\t\tFail !")
                            finally:
                                csvFile.close()
                        else:
                            pass  # 存在過檔案就跳過
    driver.quit()  # 如果使用selenium記得關掉


